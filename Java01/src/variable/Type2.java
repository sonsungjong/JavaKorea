package variable;

public class Type2 {
	public static void main(String[] args) {
		// 변수를 활용해서
		// 이름, 출생년도, 키
		
		// 예시의 OOO을 변수로 사용
		/*
		 이름 : OOO
		 출생년도 : OOOO년 
		 키 : OOO.OOcm
		 */
		String name = "홍길동";
		int birth = 2000;
		double height = 175.5;
		
		System.out.println("이름 : "+name);
		System.out.println("출생년도 : "+birth+"년");
		System.out.println("키 : "+height+"cm");
	}
}
